# Variables in Python
a = 1   # integer number 
print(a)

b = 1.1357101 # decimal number 
print(b)

c = "Rittick" # word/sentence - use quotes
print(c)

Rishabh = 26 # declare a variable using variable
d = "Rittick"
print(d)

x = 10    # adding a number to a variable 
print(x+1)

p,q,r = 11, 12, 13  # assign multiple variables
print(p, q, r)

# ways to declare a variable name
MyName = "Rittick" # pascal case
myName = "Rittick" # camel case
myname = "Rittick" # flat case
my_name = "Rittick" # snake case

# rules to declare a variable 
# 1. Variable name must start with: letter or (_)
_my_name = "Rittick"
print(_my_name)

# 2. Variable name can contain: letter, numbers, (_)
my_name1 = "Rittick"
r1 = 26
_1 = 1

# 3. Variables are case sensitive
myname = "RittickMishra"
print(myname)
 
